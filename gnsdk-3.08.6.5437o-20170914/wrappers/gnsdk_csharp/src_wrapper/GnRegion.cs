
namespace GracenoteSDK {

/**
* @enum GnRegion
* Regions for loading locale/list
*/
public enum GnRegion {
/** Default region. A region will be selected based on what's available. */
  kRegionDefault = 0,
/** Global */
  kRegionGlobal,
/** United States */
  kRegionUS,
/** Japan */
  kRegionJapan,
/** China  */
  kRegionChina,
/** Taiwan */
  kRegionTaiwan,
/** Korea */
  kRegionKorea,
/** Europe */
  kRegionEurope,
/** Deprecated, will be removed in a future release, use kRegionUS. */
  kRegionNorthAmerica,
/** Latin America */
  kRegionLatinAmerica,
/** India */
  kRegionIndia
}

}
